#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*p;

	p = malloc(ft_strlen(s1) + 1);
	if (!p)
		return (0);
	ft_memcpy(p, s1, ft_strlen(s1) + 1);
	return (p);
}
